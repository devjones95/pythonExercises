nome = 'João'
idade = 28
profissao = 'Programador'
linguagem = 'Python'

# Vamos brincar com as formatações das nossas strings variáveis

# .upper() transforma todas as letras em maiúsculas

print(nome.upper()) # resultado: JOÃO
print(profissao.upper()) # resultado: PROGRAMADOR
print(linguagem.upper()) # resultado: PYTHON
print()

#.lower() transforma todas as letras em minúsculas

print(nome.lower()) # resultado: joão
print(profissao.lower()) # resultado: programador
print(linguagem.lower()) # resultado: python
print()

# .title() coloca a primeira letra em maiúsculo

print(nome.title()) # resultado: João
print(profissao.title()) # resultado: Programador
print(linguagem.title()) # resultado: Python
print()
